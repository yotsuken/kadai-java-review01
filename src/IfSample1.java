
public class IfSample1 {

    public static void main(String[] args) {
        int score = 61; //成績
        
        //scoreが60未満でないならば
        if(score > 60 ){
            //合格です！と表示
            System.out.println("合格です！");
        } else {
            //赤点です...と表示
            System.out.println("赤点です...");
        }

        
    }

}
